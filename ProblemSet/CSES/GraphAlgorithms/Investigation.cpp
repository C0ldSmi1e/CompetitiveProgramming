template <typename A, typename B>
tuple<LL, Mint, int, int> Dijkstra(const vector<vector<pair<int, B> > > &g, int start, const A &INF) {
  int n = static_cast<int>(g.size());
  assert(start >= 0 && start < n);
  vector<A> dist(n, INF);
  dist[start] = 0;
  VE<Mint> cnt(n, 0);
  cnt[start] = 1;
  VI mx(n, -(int) 2e9), mn(n, (int) 2e9);
  mx[start] = mn[start] = 0;
  priority_queue<pair<A, int>, vector<pair<A, int> >, greater<pair<A, int> > > heap;
  heap.emplace(dist[start], start);
  while ((int) heap.size() > 0) {
    auto [d, u] = heap.top();
    heap.pop();
    if (dist[u] != d) {
      continue;
    }
    for (auto &[v, w] : g[u]) {
      if (dist[v] > w + dist[u]) {
        cnt[v] = cnt[u];
        mx[v] = mx[u] + 1;
        mn[v] = mn[u] + 1;
        dist[v] = w + dist[u];
        heap.emplace(dist[v], v);
      } else if (dist[v] == w + dist[u]) {
        cnt[v] += cnt[u];
        mx[v] = max(mx[v], mx[u] + 1);
        mn[v] = min(mn[v], mn[u] + 1);
      }
    }
  }
  // returns INF if there's no path
  return MT(dist.back(), cnt.back(), mn.back(), mx.back());
}

