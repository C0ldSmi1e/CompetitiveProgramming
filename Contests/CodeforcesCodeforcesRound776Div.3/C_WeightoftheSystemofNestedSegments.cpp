int main() {
  SOS(10);

  int T;
  cin >> T;
  while (T--) {
    int n, m;
    cin >> n >> m;
    VI x(m), w(m);
    for (int i = 0; i < m; i++) {
      cin >> x[i] >> w[i];
    }
    VI order(m);
    iota(ALL(order), 0);
    sort(ALL(order), [&](int i, int j) { return w[i] < w[j]; });
    LL res = 0;
    VPII p;
    for (int i = 0; i < 2 * n; i++) {
      res += w[order[i]];
      p.EB(x[order[i]], order[i]);
    }
    cout << res << '\n';
    sort(ALL(p));
    for (int i = 0, j = 2 * n - 1; i < n; i++, j--) {
      cout << p[i].S + 1 << ' ' << p[j].S + 1 << '\n';
    }
  }
  return 0;
}

